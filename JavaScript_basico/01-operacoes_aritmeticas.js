// + Adição
// - Subtração
// * Multiplcação
// / Divisão
// % módulo
// ++ Incremento 
// -- Decremento 

var a = 10;
var b = 15;

var c = a + b;
console.log(c);

var d = a - b;
console.log(d);

var e = a * b;
console.log(e);

var f = a / b;
console.log(f);

var g = a % b;
console.log(g);

console.log(++a); // primeiro incrementa, depois imprime 11
console.log(a++); //imprime 10, depois incrementa
console.log(--b);